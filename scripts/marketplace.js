const contractAddress = "0xDd5e79e75dF4a14550E8127Acf2AE6414E937505";

async function loadListedNFTs() {
    const nftGrid = document.getElementById('nft-grid');
    const loading = document.getElementById('loading');
    const noNfts = document.getElementById('no-nfts');

    try {
        loading.style.display = 'block';
        nftGrid.innerHTML = '';
        noNfts.style.display = 'none';

        const web3 = new Web3(window.ethereum);
        const contract = new web3.eth.Contract(contractABI, contractAddress);

        // Lấy tổng supply của NFT
        const totalSupply = await contract.methods.totalSupply().call();
        let listedNFTs = [];

        // Kiểm tra từng NFT
        for (let i = 0; i < totalSupply; i++) {
            const tokenId = await contract.methods.tokenByIndex(i).call();
            const listing = await contract.methods.getListingInfo(tokenId).call();

            if (listing.isListed) {
                const tokenURI = await contract.methods.tokenURI(tokenId).call();
                const metadata = await fetchMetadata(tokenURI);

                listedNFTs.push({
                    tokenId,
                    price: listing.price,
                    seller: listing.seller,
                    metadata
                });
            }
        }

        if (listedNFTs.length === 0) {
            noNfts.style.display = 'block';
            return;
        }

        // Hiển thị NFTs
        listedNFTs.forEach(nft => {
            const card = createNFTCard(nft);
            nftGrid.appendChild(card);
        });

    } catch (error) {
        console.error('Error loading NFTs:', error);
    } finally {
        loading.style.display = 'none';
    }
}

function createNFTCard(nft) {
    const card = document.createElement('div');
    card.className = 'nft-card';
    card.innerHTML = `
        <div class="nft-image">
            <img src="${nft.metadata.image.replace('ipfs://', 'https://ipfs.io/ipfs/')}" 
                 alt="${nft.metadata.name}">
        </div>
        <div class="nft-info">
            <h3>${nft.metadata.name}</h3>
            <p class="nft-price">${Web3.utils.fromWei(nft.price, 'ether')} TIA</p>
            <button class="buy-button" onclick="buyNFT('${nft.tokenId}', '${nft.price}')">
                Buy Now
            </button>
        </div>
    `;
    return card;
}

async function buyNFT(tokenId, price) {
    try {
        const web3 = new Web3(window.ethereum);
        const accounts = await web3.eth.requestAccounts();
        const userAddress = accounts[0];

        const contract = new web3.eth.Contract(contractABI, contractAddress);

        const buyTx = await contract.methods.buyNFT(tokenId).send({
            from: userAddress,
            value: price,
            gasPrice: '25000000000', // 25 nTIA
            gas: '250000'
        });

        if (buyTx.status) {
            showNotification('NFT purchased successfully!', 'success');
            setTimeout(() => loadListedNFTs(), 2000);
        }

    } catch (error) {
        console.error('Buy error:', error);
        showNotification(error.message, 'error');
    }
}

